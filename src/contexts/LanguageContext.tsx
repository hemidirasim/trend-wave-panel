const getNavigatorLanguage = () => {
  if (typeof window === 'undefined' || typeof window.navigator === 'undefined') {
    return 'en';
  }

  const navigatorLanguage =
    window.navigator.language ||
    (window.navigator as any).userLanguage ||
    'en'; // Default to 'en' if no language is found

  return navigatorLanguage.split('-')[0]; // Use only the primary language code
};

export const LanguageProvider: React.FC<LanguageProviderProps> = ({ children }) => {
  const [language, setLanguage] = useState<string>(() => {
    // Initialize language from localStorage or fallback to browser language
    const storedLanguage = typeof window !== 'undefined' ? localStorage.getItem('language') : null;
    return storedLanguage || getNavigatorLanguage() || 'en';
  });

  useEffect(() => {
    // Update localStorage when language changes
    localStorage.setItem('language', language);
  }, [language]);

  const t = useCallback((key: string, params?: Record<string, any>): string => {
    let translation = translations[language as keyof typeof translations]?.[key] || key;

    if (params) {
      Object.keys(params).forEach(paramKey => {
        const regex = new RegExp(`{${paramKey}}`, 'g');
        translation = translation.replace(regex, params[paramKey]);
      });
    }

    return translation;
  }, [language]);

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  );
};

export const useLanguage = () => {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
};
